// Daily puzzle generator
export class DailyPuzzleGenerator {
  private static instance: DailyPuzzleGenerator;
  
  static getInstance(): DailyPuzzleGenerator {
    if (!DailyPuzzleGenerator.instance) {
      DailyPuzzleGenerator.instance = new DailyPuzzleGenerator();
    }
    return DailyPuzzleGenerator.instance;
  }

  // Get today's Wordle word
  getTodaysWordle(): string {
    const today = new Date();
    const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24));
    const index = dayOfYear % WORDLE_WORDS.length;
    return WORDLE_WORDS[index];
  }

  // Get today's Connections puzzle
  getTodaysConnections() {
    const today = new Date();
    const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24));
    const index = dayOfYear % CONNECTIONS_PUZZLES.length;
    return CONNECTIONS_PUZZLES[index];
  }

  // Get today's Mini Crossword
  getTodaysMiniCrossword() {
    const today = new Date();
    const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24));
    const index = dayOfYear % MINI_CROSSWORD_PUZZLES.length;
    return MINI_CROSSWORD_PUZZLES[index];
  }

  // Get puzzle number for the year
  getPuzzleNumber(): number {
    const today = new Date();
    const startOfYear = new Date(today.getFullYear(), 0, 1);
    const dayOfYear = Math.floor((today.getTime() - startOfYear.getTime()) / (1000 * 60 * 60 * 24)) + 1;
    return dayOfYear;
  }
}

